#第三种创建字典的方式
d=dict(cat=10,dog=20)#左侧cat是key，右侧是值
print(d)
t=(10,20,30)#元组是不可变数据类型，不能insert，pop。remove
print({t:10})#key必须是不可变数据类型
lst=[10,20,30]#列表是可变数据类型，不能做字典的key
print(lst)
