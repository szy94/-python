#第一种创建字典的方式
d={10:'cat',20:'dog',30:'pet',20:'zoo'}
print(d)#key相同时，最后出现的key相同的value值进行了覆盖
##第二种创建字典的方式zip函数
lst1=[10,20,30,40]
lst2=['cat','dog','pet','zoo','car']
zip1=zip(lst1,lst2)
print(zip)
#print(list(zip1))
d=dict(zip1)
